n = input('type anything: ')
print('is this numeric? ', n.isnumeric())
print('is this only spaces? ', n.isspace())
print('is this alphabetic', n.isalpha())
print('is this alphanumeric', n.isalnum())
print('is this uppercase', n.isupper())
print('is this lowercase', n.islower())
print('is this titled', n.istitle())


